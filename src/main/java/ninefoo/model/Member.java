package ninefoo.model;

import ninefoo.lib.DateUtils;

import java.util.Date;

/**
 * Created by Farzad on 30-May-2015.
 */
public class Member {
    private int memberId;
    private String firstName;
    private String lastName;
    private String username;
    private String password;
    private String registerDate;

    /**
     * This constructor is only used when we want to convert db entities to Java classes
     * @param memberId ID of the member
     * @param firstName First name
     * @param lastName last name
     * @param username Username
     * @param password Password
     * @param registerDate Date of registration
     */
    Member(int memberId, String firstName, String lastName,
           String username, String password, Date registerDate) {

        this.memberId = memberId;
        this.firstName = firstName;
        this.lastName = lastName;
        this.username = username;
        this.password = password;
        this.registerDate = DateUtils.format(registerDate);
    }

    /**
     * This constructor should be used when creating a new member in Java code because
     *      the ID is generated by the DB when the member is inserted into the table.
     * @param firstName First name
     * @param lastName Last name
     * @param username Username
     * @param password Password
     */
    public Member(String firstName, String lastName, String username,
                  String password) {

        this.firstName = firstName;
        this.lastName = lastName;
        this.username = username;
        this.password = password;
    }

    public int getMemberId() {
        return memberId;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public Date getRegisterDate() {
        return DateUtils.parse(registerDate);
    }

    public String toString() {
        return String.format("Member [ID: %d, FirstName: '%s', LastName: '%s', " +
                "Username: '%s', Password: '%s', Register: '%s']", memberId,
                firstName, lastName, username, password, registerDate);
    }
}
